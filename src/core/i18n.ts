// 轻量 i18n：主要语言 / 跟随浏览器
import type { Settings } from '../types';

export type ResolvedLang = 'zh' | 'en' | 'ja' | 'ko' | 'fr' | 'de' | 'es' | 'pt' | 'ru';

/** 设置页语言下拉框选项（原生语言名） */
export const LANGUAGE_OPTIONS: { value: ResolvedLang; label: string }[] = [
  { value: 'zh', label: '简体中文' },
  { value: 'en', label: 'English' },
  { value: 'ja', label: '日本語' },
  { value: 'ko', label: '한국어' },
  { value: 'fr', label: 'Français' },
  { value: 'de', label: 'Deutsch' },
  { value: 'es', label: 'Español' },
  { value: 'pt', label: 'Português' },
  { value: 'ru', label: 'Русский' },
];

export function resolveLang(language: Settings['language']): ResolvedLang {
  if (language !== 'auto') return language;
  const tag = navigator.language.toLowerCase();
  for (const { value } of LANGUAGE_OPTIONS) {
    if (tag.startsWith(value)) return value;
  }
  return 'en';
}

