// 当前语言
let _currentLang = 'en';

// 设置当前语言
function setCurrentLang(lang) {
  if (lang === 'system') {
    const browserLang = (navigator.language || 'en').replace('-', '_');
    _currentLang = browserLang.startsWith('zh') ? 'zh_CN' : 'en';
  } else {
    _currentLang = lang;
  }
}

// 获取翻译文本，支持 $1, $2 等占位符替换
function i18n(key, substitutions) {
  let msg = I18N_MESSAGES[_currentLang]?.[key] || I18N_MESSAGES['en'][key] || key;
  
  if (substitutions && Array.isArray(substitutions)) {
    substitutions.forEach((val, idx) => {
      msg = msg.replace(`$${idx + 1}`, val);
    });
  }
  
  return msg;
}

// 初始化 i18n（从存储中加载语言设置）
async function initI18n() {
  const result = await chrome.storage.local.get('language');
  const lang = result.language || 'system';
  setCurrentLang(lang);
  applyI18n();
}

// 应用翻译到所有带 data-i18n 属性的元素
function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const msg = i18n(key);
    if (msg) el.textContent = msg;
  });
  
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    const msg = i18n(key);
    if (msg) el.setAttribute('title', msg);
  });
  
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    const msg = i18n(key);
    if (msg) el.setAttribute('placeholder', msg);
  });
  
  // 更新 select options
  document.querySelectorAll('select option[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const msg = i18n(key);
    if (msg) el.textContent = msg;
  });
}

// 监听语言变化
chrome.storage.onChanged.addListener((changes, area) => {
  if (area === 'local' && changes.language) {
    const newLang = changes.language.newValue || 'system';
    setCurrentLang(newLang);
    applyI18n();
  }
});
